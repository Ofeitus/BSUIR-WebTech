package by.bsuir.webtech.lab1.task9to11.util;

public class Ball {
    private Color color;
    private double mass;

    public Ball(Color color, double mass) {
        this.color = color;
        this.mass = mass;
    }

    public Color getColor() {
        return color;
    }

    public double getMass() {
        return mass;
    }
}
